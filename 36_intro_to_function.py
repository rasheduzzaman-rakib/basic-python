def add(x,y): #must use def for create function
    sum=x+y
    print(sum)


def addition(x,y,z):#three digit addition
    sum=x+y+z
    print(sum)
    
    
def sub(x,y):#subtraction
    result=y-x
    print(result)
    
def message():#message
    print("no parameter")
    

def multiply(x,y):#multiplication + addition
    result=x*x + y*y
    print(result)
    
    
